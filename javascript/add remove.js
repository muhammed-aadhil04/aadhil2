let fruits=["apple","banana","mango"]
// push 
fruits.push("orange")
console.log(fruits)
// pop 
fruits.pop()
console.log(fruits)
// unshift 
fruits.unshift("kiwi")
console.log(fruits)
// shift
fruits.shift()
console.log(fruits)
// splice 
fruits.splice(1,1,"jackfruit","watermelon")
console.log(fruits)
// slice 
let citrus=fruits.slice(1,3)
console.log(citrus)

